-- Trigger anti-solapamiento en Oracle:
CREATE OR REPLACE TRIGGER TRG_COLA_NO_SOLAPAMIENTO
BEFORE INSERT OR UPDATE ON COLAMAQUINA FOR EACH ROW
DECLARE
    v_cnt NUMBER;
BEGIN
    IF :NEW.ESTADO = 'EN_PROCESO' THEN
        SELECT COUNT(*) INTO v_cnt
        FROM COLAMAQUINA
        WHERE MAQUINA_ID = :NEW.MAQUINA_ID
          AND ESTADO     = 'EN_PROCESO'
          AND COLA_ID   <> NVL(:NEW.COLA_ID, -1);
        IF v_cnt > 0 THEN
            RAISE_APPLICATION_ERROR(-20001,
                'La máquina ' || :NEW.MAQUINA_ID || ' ya tiene un trabajo EN_PROCESO');
        END IF;
    END IF;
END;
/

